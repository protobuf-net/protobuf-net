        public static string GetPrettyHex(byte[] bytes)
        {
            var sb = new StringBuilder();
            int left = bytes.Length, offset = 0;
            while(left > 0)
            {
                int take = Math.Min(left, 16);
                sb.AppendLine(BitConverter.ToString(bytes, offset, take));
                left -= take;
                offset += take;
            }
            return sb.ToString();
        }
